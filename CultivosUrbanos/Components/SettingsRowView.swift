
import SwiftUI

struct SettingsRowView: View {
    let imageName: String
    let title: String
    let tintColor: Color
    
    var body: some View {
        HStack(spacing: 12){
            Image(systemName: imageName)
                .imageScale(.small)
                .font(.title)
                .foregroundColor(Color.colorText)
            Text(title)
                .font(.subheadline)
                .foregroundColor(Color.colorText)
        }
    }
}

